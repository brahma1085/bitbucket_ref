package edu.pojo;

public class Stud {
	private String stdNo;
	private String stdName;
	private String stdAge;
	private String stdMarks;
	private String stdQual;

	public String getStdNo() {
		return stdNo;
	}

	public void setStdNo(String stdNo) {
		this.stdNo = stdNo;
	}

	public String getStdName() {
		return stdName;
	}

	public void setStdName(String stdName) {
		this.stdName = stdName;
	}

	public String getStdAge() {
		return stdAge;
	}

	public void setStdAge(String stdAge) {
		this.stdAge = stdAge;
	}

	public String getStdMarks() {
		return stdMarks;
	}

	public void setStdMarks(String stdMarks) {
		this.stdMarks = stdMarks;
	}

	public String getStdQual() {
		return stdQual;
	}

	public void setStdQual(String stdQual) {
		this.stdQual = stdQual;
	}
}
