public class PrintfIllustration {
	public static void main(String[] args) {
		char ch1, ch2;
		ch1 = 'A';
		ch2 = '2';
		int a = 345;
		System.out.printf("\n %d", (int) ch1);
		System.out.printf("\n %c", ch1);
		System.out.printf("\n %d", (int) ch2);
		System.out.printf("\n %c", ch2);
		System.out.printf("\n %x", a);
		System.out.printf("\n %o", a);
		System.out.printf("\n %d", a);
		System.out.printf("\n %s", a);
		System.out.printf("\n %f", a);
		System.out.printf("\n %e", a);
		System.out.printf("\n %c", a);
	}
}
